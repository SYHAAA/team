public class UserExam {
    private Integer subjectId;
    private String answer;
    private String referenceAnswer;
    private String analytical;
    private Integer subjectSource;

    public Integer getSubjectSource() {
        return subjectSource;
    }

    public void setSubjectSource(Integer subjectSource) {
        this.subjectSource = subjectSource;
    }

    public String getAnalytical() {
        return analytical;
    }

    public void setAnalytical(String analytical) {
        this.analytical = analytical;
    }

    public Integer getSubjectId() {
        return subjectId;
    }

    public void setSubjectId(Integer subjectId) {
        this.subjectId = subjectId;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }

    public String getReferenceAnswer() {
        return referenceAnswer;
    }

    public void setReferenceAnswer(String referenceAnswer) {
        this.referenceAnswer = referenceAnswer;
    }
}
